package com.beijunyi.sw.game.model;

public class CharacterPosition {
  private int scene;
  private int east;
  private int south;

  public int getScene() {
    return scene;
  }

  public void setScene(int scene) {
    this.scene = scene;
  }

  public int getEast() {
    return east;
  }

  public void setEast(int east) {
    this.east = east;
  }

  public int getSouth() {
    return south;
  }

  public void setSouth(int south) {
    this.south = south;
  }
}
